package br.lopes.unittest.model;

import android.support.annotation.NonNull;

/**
 * The type Contact.
 */
public class Contact {

    private int age;
    private String email;
    private String name;
    private String rg;

    public Contact(@NonNull String rg) {
        this.rg = rg;
    }

    /**
     * Gets age.
     *
     * @return the age
     */
    public int getAge() {
        return age;
    }

    /**
     * Sets age.
     *
     * @param age the age
     */
    public void setAge(int age) {
        this.age = age;
    }

    /**
     * Gets email.
     *
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * Sets email.
     *
     * @param email the email
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets name.
     *
     * @param name the name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets rg.
     *
     * @return the rg
     */
    public String getRg() {
        return rg;
    }

}
